
public class ForLoop {
	public static void main(String[] args) {
		//Task : 1
		//Transeverse in forward direction
		for(int i=1; i<=5;i++) {
			System.out.println(i);
		}
		//Task : 2
		//In reverse direction
		for(int j=5;j>=1;j--) {
			System.out.println(j);
		}
		
		int a[]= {1,2,3,4,5,6};
		for(int z=0;z<a.length;z++) {
			System.out.println(a[z]);
		}
	}
}

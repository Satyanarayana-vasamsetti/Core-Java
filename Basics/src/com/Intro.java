package com;

public class Intro {
	public static void main(String[] args) {
		System.out.println("Hello,World!");
		
		//Data types
		//Mobile Oneplus
		String brand = "Oneplus";
		int ram = 8;
		double price = 24850.00;
		long serialno = 1234567890;
		boolean isAvailable = true;
		float rating = 4.3f;
		
		//variables
		int a = 10;
//		where here "a" is a variable to store a value
		
		//literals (constant value)
		int b = 20;
//		Here 
//		int=>data type
//		b=>variable
//		=:operator
//		20=>literal(constant value)
		
		System.out.println(a);
		
		//binary 
		int aa = 0b101;
		System.out.println(aa);
		
		//hexa base 16 prefix 0x
		int aaa = 0xf23;
		System.out.println(aaa);
		 
		//octal base 8 prefix 0
		int aaaa = 076;
		System.out.println(aaaa);
		
	}
}

//length vs length()
public class task2 {
	public static void main(String[] args) {
		char ch[] = {'b','u','n','n','y'};
		System.out.println(ch.length);
		
		String s = "Satyanarayana";
		System.out.println(s.length());
		
		String b = new String("Satyadevi");
		System.out.println(b.length());
	}
}

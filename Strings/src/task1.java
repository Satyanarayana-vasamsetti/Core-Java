//String is a sequence of characters which is a immutable
public class task1 {
	public static void main(String[] args) {
		//array approach 
		char ch[] = {'a','e','i','o','u'};
		System.out.println(ch);
		
		//String literal
		String name = "Bunny";
		System.out.println(name);
		
		//using new keyword
		String s = new String("satya");
		System.out.println(s);
	}
}
